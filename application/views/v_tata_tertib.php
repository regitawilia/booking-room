
<br>
<p><b>Peraturan ruangan</b> </p>
<ol type=1>
	<li>Pengguna tertib di dalam ruangan; </li>
	<li>Pengguna menjaga kebersihan di dalam ruangan; </li>
	<li>Pengguna dilarang makan dan minum di dalam ruangan; </li>
	<li>Pengguna dilarang merokok di dalam ruangan;</li>
	<li>Pengguna wajib menjaga fasilitas di dalam ruangan dan tidak memindahkan posisi barang tanpa sepengetahuan petugas;</li>
	<li>Pengguna merapikan kembali ruangan setelah selesai digunakan;</li>
	<li>Dilarang menggunakan fasilitas ruangan untuk kepentingan komersial dan politik.</li>
</ol>